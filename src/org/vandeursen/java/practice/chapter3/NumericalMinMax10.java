public class NumericalMinMax10 {

    public static void main(String[] args) {
        System.out.println("Integer Max Value: " + Integer.MAX_VALUE);
        System.out.println("Integer Min Value: " + Integer.MIN_VALUE);
        System.out.println("Integer memory size: " + Integer.SIZE);
        System.out.println("Byte Max Value: " + Byte.MAX_VALUE);
        System.out.println("Byte Min Value: " + Byte.MIN_VALUE);
        System.out.println("Byte memory size: " + Byte.SIZE);
        System.out.println("Long memory size: " + Long.SIZE);
        System.out.println("Float memory size: " + Float.SIZE);
        System.out.println("Double memory size: " + Double.SIZE);
        System.out.printf("Float smallest value: %3.3f\n", Float.MIN_VALUE);
    }

}
